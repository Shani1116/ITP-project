    private void tabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabMouseClicked
        // TODO add your handling code here:
        
        int x= tab.getSelectedRow();
        a.setAdvertismentID(tab.getValueAt(x, 0).toString());
        
        String sq="SELECT a.period,a.description,a.adType,l.location,l.locTy FROM advertisment a,dis_location l WHERE a.locID=l.locationID AND a.ad_id='"+a.getAdvertismentID()+"'";
        
        try{
                        pst = con.prepareStatement(sq); //need to  access three tables 
                        rs = pst.executeQuery();
                        rs.next();
                 
                        loc.setText(rs.getString("location"));
                        dt.setText(rs.getString("locTy"));
                        des.setText(rs.getString("description"));
                        at.setText(rs.getString("adType"));
                        per.setText(rs.getString("period"));
                        
                        loc.setEditable(false);
                        dt.setEditable(false);
                        des.setEditable(false);
                        at.setEditable(false);
                        
                        
                 }
                 catch(SQLException e){
                 
                      JOptionPane.showMessageDialog(null,"Incorrect Value"+e);
                 
                 }
    }//GEN-LAST:event_tabMouseClicked

