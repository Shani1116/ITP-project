    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int k=tab.getSelectedRow();
        if(per.getText().isEmpty()){
         JOptionPane.showMessageDialog(null,"Please fill all the fields");
        }
        else{
            int x= JOptionPane.showConfirmDialog(null,"Do you  really want to  update?");
       
            if(x==0){
                Validation v = new Validation();
                
             if(!v.isNumeric(per.getText())){
                   JOptionPane.showMessageDialog(null,"Enter a again!");
             }
                  
            }
             else{
                a.setPeriod(Integer.parseInt(per.getText()));
                a.setAdvertismentID(tab.getValueAt(k, 0).toString());
                a.setLocation(loc.getText());
                a.setType(dt.getText());
                a.setaType(at.getText());
        
                 ap.upPay(a.getAdvertismentID(),a.getLocation(),a.getType(),a.getaType(),a.getPeriod());
                 
             }
        
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed


