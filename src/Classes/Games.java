package Classes;

// .*;


public class Games {
    public String Name;
    public String GID;
    public float Fee;

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public String getGID() {
        return GID;
    }

    public void setGID(String GID) {
        this.GID = GID;
    }

    public float getFee() {
        return Fee;
    }

    public void setFee(float Fee) {
        this.Fee = Fee;
    }
    
}
