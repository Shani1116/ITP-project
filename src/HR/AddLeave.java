    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(eid.getText().isEmpty() || sdate.getDate().toString().isEmpty() || edate.getDate().toString().isEmpty() || type.getSelectedItem().toString().isEmpty() || work.getSelectedItem().toString().isEmpty() || reason.getText().isEmpty() || dur.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill all the fields");
        }
        SimpleDateFormat d = new SimpleDateFormat("yyyy-MM-dd");
        
        
        l.empID = eid.getText();
        
        l.sdate = d.format(sdate.getDate());
        //l.sdate = sdate.getDate().toString();
        l.edate = d.format(edate.getDate());
        l.type = type.getSelectedItem().toString();
        l.workplace = work.getSelectedItem().toString();
        l.reason = reason.getText();
        l.duration = Integer.parseInt(dur.getText());
        
        
        
        
       h.addleave(l.empID,l.sdate,l.edate,l.type,l.workplace,l.reason,l.duration);
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

