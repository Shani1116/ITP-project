    public void tableload(){
        
        String v = select.getDate().toString();
        
        try{
        
        String sql = "select  category,Amount from expenses where date LIKE '%"+ v +"%' ";
        pst = conn.prepareStatement(sql);
        rs=pst.executeQuery(sql);
        jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        
        }
        catch(Exception e){
        
            System.out.println(e);
        }
      
    
    }
    
    public void setIDBox(String cat,double amnt,String dat)
     {
           String sq = "SELECT expenseID FROM expenses WHERE category='"+cat+"' AND Amount='"+amnt+"' AND date='"+dat+"'";
      
           try{
         
         pst = conn.prepareStatement(sq);
         rs=pst.executeQuery(sq);
         rs.next();
         
          jTextField1.setText(rs.getString("expenseID"));
          
    
        }
        
        catch (SQLException e){
        
        JOptionPane.showMessageDialog(null,e);
        
        }
     }
    

