    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

         if(jTextField1.getText().isEmpty() || amountbox.getText().isEmpty() || categorybox.getSelectedIndex()==-1 ){
            JOptionPane.showMessageDialog(null,"Cannot contain empty fields, fill all the fields"); }
        
         else{
        e.expenseID=jTextField1.getText();
        e.category = categorybox.getSelectedItem().toString();
        e.Amount = Double.parseDouble(amountbox.getText());
        e.date = datebox.getDate().toString();

        f.addExpense(e.expenseID,e.category,e.Amount,e.date);
        tableload();
         }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int r = jTable1.getSelectedRow();

        e.category=jTable1.getValueAt(r, 0).toString();
        e.Amount=Double.parseDouble(jTable1.getValueAt(r, 1).toString());

        jTextField1.setText(e.expenseID);
        categorybox.setSelectedItem(e.category);
        amountbox.setText(jTable1.getValueAt(r, 1).toString());
        setIDBox(e.category, e.Amount, select.getDate().toString());
    }//GEN-LAST:event_jTable1MouseClicked

