    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
             if(jTextField2.getText().isEmpty() || jTextField5.getText().isEmpty() || jTextField4.getText().isEmpty() || jTextField1.getText().isEmpty() || jTextField3.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Cannot contain empty fields, fill all the fields"); }
             
             else{   
             
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        m.startdate = dateFormat.format(jDateChooser1.getDate());
        SimpleDateFormat dateFormat1 = new SimpleDateFormat("yyyy-MM-dd");
        m.enddate=dateFormat1.format(jDateChooser2.getDate());
        m.Current_Assets=Double.parseDouble(jTextField2.getText());
        m.Current_Liabilities=Double.parseDouble(jTextField5.getText());
        m.Fixed_Assets=Double.parseDouble(jTextField4.getText());
        m.LongTerm_Liabilities=Double.parseDouble(jTextField1.getText());
        m.Shareholders_Equity=Double.parseDouble(jTextField3.getText()); 
        
        f.addMallDetails(m.startdate,m.enddate,m.Current_Assets,m.Current_Liabilities,m.Fixed_Assets,m.LongTerm_Liabilities,m.Shareholders_Equity);
             }
    }//GEN-LAST:event_jButton3ActionPerformed

